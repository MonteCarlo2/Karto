/**
 * Умный выбор стиля и фона на основе названия товара
 * 
 * КАК РАБОТАЕТ:
 * 1. Берет название товара (например, "Кашпо пластиковое")
 * 2. Преобразует в нижний регистр для поиска
 * 3. Проверяет, содержит ли название ключевые слова из разных категорий:
 *    - Электроника: "телевизор", "монитор", "ноутбук" → технологичный стиль
 *    - Растения: "кашпо", "горшок", "растение" → природный стиль
 *    - Мебель: "стол", "стул", "диван" → домашний стиль
 *    - И т.д.
 * 4. Возвращает подходящее описание стиля для промпта
 * 
 * ПРИМЕР:
 * - Вход: "Кашпо пластиковое черное"
 * - Находит: "кашпо" → категория "Растения"
 * - Возвращает: "Природный стиль: светлый фон с элементами природы..."
 */
function getSmartStyleForProduct(productName: string): string {
  const name = productName.toLowerCase();
  
  // Электроника и техника
  if (name.includes("телевизор") || name.includes("монитор") || name.includes("экран") || 
      name.includes("ноутбук") || name.includes("планшет") || name.includes("смартфон") ||
      name.includes("телефон") || name.includes("компьютер")) {
    return "Современный технологичный стиль: темный или светлый фон с геометрическими элементами, неоновые акценты, футуристическая атмосфера. Подходит для техники.";
  }
  
  // Мебель и интерьер
  if (name.includes("стол") || name.includes("стул") || name.includes("кровать") ||
      name.includes("диван") || name.includes("шкаф") || name.includes("полка") ||
      name.includes("кресло") || name.includes("мебель")) {
    return "Уютный домашний стиль: теплые тона, натуральные материалы (дерево, текстиль), мягкое освещение, домашняя атмосфера.";
  }
  
  // Растения и сад
  if (name.includes("кашпо") || name.includes("горшок") || name.includes("растение") ||
      name.includes("цветок") || name.includes("сад") || name.includes("рассада")) {
    return "Природный стиль: светлый фон с элементами природы (листья, растения), натуральные материалы, свежая атмосфера.";
  }
  
  // Одежда и текстиль
  if (name.includes("футболка") || name.includes("рубашка") || name.includes("платье") ||
      name.includes("одежда") || name.includes("ткань") || name.includes("текстиль")) {
    return "Стильный минималистичный фон: нейтральные тона, элегантная композиция, акцент на товаре.";
  }
  
  // Косметика и красота
  if (name.includes("крем") || name.includes("шампунь") || name.includes("косметика") ||
      name.includes("макияж") || name.includes("парфюм")) {
    return "Премиальный стиль: элегантный фон, мягкие пастельные тона или контрастные цвета, люксовый вид.";
  }
  
  // Спорт и активность
  if (name.includes("кроссовки") || name.includes("спорт") || name.includes("тренировка") ||
      name.includes("фитнес") || name.includes("мяч")) {
    return "Динамичный стиль: яркие цвета, энергичная композиция, современный спортивный вид.";
  }
  
  // По умолчанию - универсальный стиль
  return "Современный профессиональный стиль: стильный фон, который дополняет товар, приятная цветовая гамма, качественное освещение.";
}

/**
 * Генерация уникальной концепции для вариации карточки
 * 
 * КАК РАБОТАЕТ:
 * 1. Принимает номер вариации (0, 1, 2, 3) - это индекс карточки в сетке
 * 2. Есть 4 предустановленные концепции композиции:
 *    - 0: Классическая (товар по центру, текст сверху)
 *    - 1: Асимметричная (товар в стороне, текст с другой стороны)
 *    - 2: Минималистичная (много пространства, чистый дизайн)
 *    - 3: Яркая (смелое размещение, контрастные цвета)
 * 3. Использует оператор % (остаток от деления) для циклического выбора:
 *    - variationIndex % 4 всегда даст 0, 1, 2 или 3
 *    - Это гарантирует, что даже если вариаций больше 4, они будут циклически повторяться
 * 4. Возвращает описание концепции для промпта
 * 
 * ПРИМЕР:
 * - Вход: variationIndex = 0 (первая карточка)
 * - Выбирает: concepts[0] = "Классическая композиция"
 * - Возвращает: "Классическая композиция: Товар по центру, текст сверху..."
 * 
 * - Вход: variationIndex = 1 (вторая карточка)
 * - Выбирает: concepts[1] = "Асимметричная композиция"
 * - Возвращает: "Асимметричная композиция: Товар смещен в сторону..."
 * 
 * ЦЕЛЬ: При генерации 4 карточек каждая получит свою уникальную концепцию композиции,
 * что сделает их визуально разными, но с одним товаром и текстом.
 */
function getVariationConcept(variationIndex: number, productName: string): string {
  const concepts = [
    {
      name: "Классическая композиция",
      description: "Товар по центру, текст сверху, традиционная компоновка"
    },
    {
      name: "Асимметричная композиция",
      description: "Товар смещен в сторону, текст размещен с противоположной стороны, динамичная композиция"
    },
    {
      name: "Минималистичная композиция",
      description: "Много свободного пространства, товар и текст аккуратно размещены, чистый дизайн"
    },
    {
      name: "Яркая композиция",
      description: "Смелое размещение элементов, контрастные цвета, запоминающийся дизайн"
    }
  ];
  
  // variationIndex % 4 гарантирует, что индекс всегда будет 0, 1, 2 или 3
  const concept = concepts[variationIndex % concepts.length];
  return `${concept.name}: ${concept.description}.`;
}

